    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // remove button
        int ri=table.getSelectedRow();
        if(ri!=-1){
            int r=JOptionPane.showConfirmDialog(null,"Do you really wanna delete ? ","Delete Confirmation ", JOptionPane.YES_NO_OPTION);
      if(r==JOptionPane.YES_OPTION){
            int id=(int)table.getValueAt(ri,0);
            try {
                Db.DbConnect.st.executeUpdate("delete from spendings where sid="+id);
                JOptionPane.showMessageDialog(null, "deleted Successfully");
                getEntries();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
      }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

