   private void displayCategory(){
       // combo box
        try{
            combo.removeAllItems();
          ResultSet rs=  Db.DbConnect.st.executeQuery("select * from catagory_info");
          
          while(rs.next()){
              combo.addItem(rs.getString("catagory"));
          }
        }catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
        }
    
    }
   private void getEntries(){
   
       try{
           javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)table.getModel();
           int rc=dtm.getRowCount();
           while(rc--!=0){
               dtm.removeRow(0);
           }
           java.time.LocalDate cd=java.time.LocalDate.now();
           java.time.LocalDate bd=cd.minusDays(20);
           ResultSet rs=Db.DbConnect.st.executeQuery("select * from spendings where date<='"+cd+"' and date>='"+bd+"'");
           int total=0;
           while(rs.next()){
               total=total+rs.getInt("amount");
               Object o[]={rs.getInt("sid"),rs.getString("category"),rs.getDate("date"),rs.getInt("amount")};
               
               dtm.addRow(o);
               ta.setText(total+"");
           }
       }catch(Exception ex){
          JOptionPane.showMessageDialog(null, ex);
       }
   }

