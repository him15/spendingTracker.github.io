    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // add button
        
        try{
            java.util.Date dt=d.getDate();
            String s1=a.getText();
            String cat=(String)combo.getSelectedItem();
            if(dt!=null && !s1.equals("") && !cat.equals("")){
                int amount=Integer.parseInt(s1);
                
                java.sql.Date date=new java.sql.Date(dt.getTime());
                Db.DbConnect.st.executeUpdate("insert into spendings (category , date , amount) values ('"+cat+"','"+date+"',"+amount+")");
                 JOptionPane.showMessageDialog(null,"Added Successfully!");
                 getEntries();
            
            }else{
                JOptionPane.showMessageDialog(null,"Enter Details First");
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

